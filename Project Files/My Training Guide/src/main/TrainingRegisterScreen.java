    private static final int SUCCESS = 0;
    private static final int INVALID_ID = 1;
    private static final int INVALID_EMAIL = 2;
    private static final int INVALID_PHONE = 3;
    
    public static int userExist(String ID,String phone, String email)
    {
        int status = 0;
        con = DBConnection.getConnection();
        try {
            PreparedStatement checkID = con.prepareStatement("select * inst from training_inst where ins_id='"+ID+"'");
            PreparedStatement checkPhone = con.prepareStatement("select * from training_inst where phone='"+phone+"'");
            PreparedStatement checkEmail = con.prepareStatement("select * from training_inst where email='"+email+"'");
            ResultSet rs1 = checkID.executeQuery();
            ResultSet rs2 = checkPhone.executeQuery();
            ResultSet rs3 = checkEmail.executeQuery();
            if(rs1.next())
            {
                status = INVALID_ID;
            }
            else if(rs2.next())
            {
                status = INVALID_PHONE;
            }
            else if(rs3.next())
            {
                status = INVALID_EMAIL;
            }
            else
            {
                status = SUCCESS;
            }
            
            
        } catch (SQLException ex) {
            System.out.println(ex);
        }
        return status;
    }

