    private void registerBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_registerBtnMouseClicked
        String name = fullname.getText();
        String userEmail = email.getText();
        String userPhone = phone.getText();
        String password = pass.getText();
        String sectorName = sector.getText();
        String myID = id.getText();
        
        
        if(name.equals("") || userEmail.equals("") || userPhone.equals("") || password.equals("") || sectorName.equals("") || myID.equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "يرجى ملء كل النص بشكل صحيح");
        }
        else if(!password.equals(pass1.getText()))
        {
            JOptionPane.showMessageDialog(rootPane, "كلمة المرور خاطئة");
        }
        else
        {
            int userCheck = userExist(myID, userPhone, userEmail);
            if(userCheck == SUCCESS)
            {
                try {
                    con = DBConnection.getConnection();
                    pst = con.prepareStatement("insert into training_inst (ins_name,ins_id,sector,email,phone,password)"
                            + " values(?,?,?,?,?,?)");
                    pst.setString(1,name);
                    pst.setString(2,myID);
                    pst.setString(3,sectorName);
                    pst.setString(4,userEmail);
                    pst.setString(5,userPhone);
                    pst.setString(6,password);
                    pst.executeUpdate();


                    int ok = JOptionPane.showConfirmDialog(rootPane, "الذهاب إلى صفحة تسجيل الدخول وتسجيل الدخول مرة أخرى؟",
                            "تم بنجاح التسجيل",JOptionPane.OK_OPTION);
                    if(ok == 0)
                    {
                        this.dispose();
                        new TrainingStartScreen().setVisible(true);
                    }
                    else
                    {
                        this.dispose();
                        new TrainingStartScreen().setVisible(true);
                    }
                }catch (Exception e) {
                    System.out.println(e);
                }
            }
            else if(userCheck == INVALID_ID)
            {
                JOptionPane.showMessageDialog(rootPane, "يوجد بالفعل مستخدم بنفس رقم الهوية","Failed",JOptionPane.ERROR_MESSAGE);
            }
            else if(userCheck == INVALID_PHONE)
            {
                JOptionPane.showMessageDialog(rootPane, "يوجد بالفعل مستخدم بنفس رقم الجوال","Failed",JOptionPane.ERROR_MESSAGE);
            }
            else if(userCheck == INVALID_EMAIL)
            {
                JOptionPane.showMessageDialog(rootPane, "يوجد بالفعل مستخدم بنفس الايميل ","Failed",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_registerBtnMouseClicked

